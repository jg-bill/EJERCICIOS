#7.-Se necesita obtener el promedio simple de un estudiante a partir de sus tres notas parciales.
n1=int(input("Ingrese la primera nota: "))
n2=int(input("Ingrese la segunda nota: "))
n3=int(input("Ingrese la tercera nota: "))
p=(n1+n2+n3)/3
print ("PROMEDIO DEL ESTUDIANTE")
print ("-"*30)
print ("Promedio: ", round(p,2))